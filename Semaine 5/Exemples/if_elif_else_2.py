
def choix_nombre(nombre: int):

    if nombre == 1:
        print("choix 1")
    elif nombre == 2:
        print("choix 2")
    elif nombre == 3:
        print("choix 3")
    else:
        print("Choix invalide")


choix_nombre(1)
choix_nombre(2)
choix_nombre(3)
choix_nombre(4)
choix_nombre(42)
choix_nombre("3")

