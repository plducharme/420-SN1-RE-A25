import numpy as np

# Constante pi
print(np.pi)
# Racine carré
print(np.sqrt(49))
# Constante nombre Euler
print(np.e)
# Exponentiel
print(np.exp(5))
# Logarithme naturel
print(np.log(np.e))
# Logarithme base 10
print(np.log10(1000))
# Sinus
print(np.sin(np.pi))
# Cosinus de 45 degrés converti en radians
print(np.cos(np.radians(45)))
# Arc cosinus
print(np.arccos(1))



